static inline int
lc (char c)
{
  if (c >= 0 && c < sizeof(lctab))
    c += lctab[(int)c];
  return c;
}

int
c_strncasecmp (char const *a, char const *b, size_t n)
{
  size_t i;

  for (i = 0; i < n; i++)
    {
      int c = lc (a[i]) - lc (b[i]);
      if (c || a[i] == 0 || b[i] == 0)
	return c;
    }
  return 0;
}

int
c_strcasecmp (char const *a, char const *b)
{
  while (*a)
    {
      if (*b == 0)
	return *(unsigned char*)a;
      else
	{
	  int c = lc (*a++) - lc (*b++);
	  if (c)
	    return c;
	}
    }
  return - *(unsigned char*)b;
}

